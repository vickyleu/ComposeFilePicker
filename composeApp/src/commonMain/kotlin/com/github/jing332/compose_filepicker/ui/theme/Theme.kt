@Composable
fun ComposefilepickerTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    // Dynamic color is available on Android 12+
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit
) {
    val colorScheme = dynamicColorScheme(LocalPlatformContext.current, darkTheme,dynamicColor)

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}

@Composable
expect fun dynamicColorScheme(context: PlatformContext, darkTheme: Boolean, dynamicColor: Boolean): ColorScheme